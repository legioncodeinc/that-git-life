#!/usr/bin/env node
import { existsSync } from "node:fs";
import { extname, join } from "node:path";
import { ensureDir, jsonOut, listFilesRecursive, nextPrdNumber, parseArgs, rel, titleCaseFromSlug, toSlug, writeIfMissing } from "./tgl-utils.mjs";

function usage() {
  console.log("Usage: node scripts/tgl-backwards-prd.mjs --root <repo> --title <title> [--scope <path>] [--lifecycle backlog|completed]");
}

function isCodeFile(file) {
  return new Set([".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs", ".py", ".go", ".rs", ".rb", ".php", ".java", ".cs", ".swift", ".kt", ".vue", ".svelte"]).has(extname(file));
}

function scopedFiles(root, scope) {
  const base = join(root, scope || ".");
  if (!existsSync(base)) return [];
  const ignored = new Set([".git", "node_modules", ".agents", ".codex", "library", "dist", "build", ".next", "coverage"]);
  return listFilesRecursive(base)
    .filter((file) => !rel(root, file).split("/").some((part) => ignored.has(part)))
    .filter(isCodeFile)
    .slice(0, 80);
}

const args = parseArgs(process.argv.slice(2));
if (args.help) {
  usage();
  process.exit(0);
}
if (!args.title) throw new Error("Missing --title");

const lifecycle = args.lifecycle === "completed" ? "completed" : "backlog";
const number = nextPrdNumber(args.root);
const slug = toSlug(args.title);
const title = titleCaseFromSlug(slug);
const dir = join(args.root, "library", "requirements", lifecycle, `prd-${number}-${slug}`);
const index = join(dir, `prd-${number}-${slug}-index.md`);
const files = scopedFiles(args.root, args.scope || ".");
const month = new Date().toLocaleString("en-US", { month: "long", year: "numeric" });

ensureDir(join(dir, "qa"));

const evidenceRows = files.length
  ? files.map((file) => `| \`${rel(args.root, file)}\` | Inspect and summarize current behavior. |`).join("\n")
  : "| _No code files detected in scope._ | Confirm whether scope is correct. |";

const content = `# PRD-${number}: ${title} *(Retroactive)*

> **Status:** ${lifecycle === "completed" ? "Shipped" : "Backlog"}
> **Priority:** - *(retroactive - work is done)*
> **Written:** ${month}
> **Retroactive:** Yes - this PRD was written after implementation.
> **Scope:** ${args.scope || "."}

---

## What was built

<!-- Codex must inspect the evidence map below and replace this with a concise description of existing behavior. -->

---

## Current behavior acceptance criteria

<!-- Replace these with behavior-preservation criteria discovered from the code. -->

| ID | Criterion |
|---|---|
| AC-1 | Given the existing implementation in scope, when its documented happy path is used, then the current behavior remains preserved by future work. |

---

## Evidence map

| Source | What to inspect |
|---|---|
${evidenceRows}

---

## APIs, data, and user surfaces

<!-- Document routes, exported functions, UI flows, schemas, and persistent data touched by this module. -->

---

## Known gaps or follow-up risks

<!-- List missing tests, unclear behavior, dead code, or future PRDs this reverse-documentation revealed. -->

---

## Related

- Generated by That Git Life backwards-PRD flow.
`;

writeIfMissing(index, content);
writeIfMissing(join(dir, "qa", "README.md"), "# QA\n\nQuality reports for this retroactive PRD live here.\n");

jsonOut({
  ok: true,
  type: "backwards-prd",
  number,
  slug,
  lifecycle,
  dir: rel(args.root, dir),
  index: rel(args.root, index),
  scope: args.scope || ".",
  evidenceFiles: files.map((file) => rel(args.root, file)),
});
