#!/usr/bin/env node
import { join } from "node:path";
import { ensureDir, jsonOut, parseArgs, readTemplate, writeIfMissing } from "./tgl-utils.mjs";

function usage() {
  console.log("Usage: node scripts/tgl-bootstrap-library.mjs --root <repo>");
}

const args = parseArgs(process.argv.slice(2));
if (args.help) {
  usage();
  process.exit(0);
}

const dirs = [
  "library",
  "library/knowledge",
  "library/knowledge/public",
  "library/knowledge/public/overview",
  "library/knowledge/public/guides",
  "library/knowledge/public/faqs",
  "library/knowledge/private",
  "library/knowledge/private/architecture",
  "library/knowledge/private/standards",
  "library/requirements",
  "library/requirements/backlog",
  "library/requirements/in-work",
  "library/requirements/completed",
  "library/requirements/reports",
  "library/issues",
  "library/issues/backlog",
  "library/issues/in-work",
  "library/issues/completed",
  "library/notes",
];

const readmes = {
  "library/README.md": "library-README.md",
  "library/knowledge/README.md": "knowledge-README.md",
  "library/knowledge/public/README.md": "knowledge-public-README.md",
  "library/knowledge/private/README.md": "knowledge-private-README.md",
  "library/requirements/README.md": "requirements-README.md",
  "library/requirements/backlog/README.md": "requirements-backlog-README.md",
  "library/requirements/in-work/README.md": "requirements-in-work-README.md",
  "library/requirements/completed/README.md": "requirements-completed-README.md",
  "library/requirements/reports/README.md": "requirements-reports-README.md",
  "library/issues/README.md": "issues-README.md",
  "library/issues/backlog/README.md": "issues-backlog-README.md",
  "library/issues/in-work/README.md": "issues-in-work-README.md",
  "library/issues/completed/README.md": "issues-completed-README.md",
  "library/notes/README.md": "notes-README.md",
};

const createdDirs = [];
const createdFiles = [];
for (const dir of dirs) {
  ensureDir(join(args.root, dir));
  createdDirs.push(dir);
}

writeIfMissing(
  join(args.root, "library", "knowledge", "private", "architecture", "README.md"),
  "# Architecture Decisions\n\nADRs live here as `ADR-<n>-<kebab-slug>.md`.\n",
) && createdFiles.push("library/knowledge/private/architecture/README.md");

writeIfMissing(
  join(args.root, "library", "knowledge", "public", "overview", "README.md"),
  "# Public Overview\n\nCustomer-facing overview documents live here.\n",
) && createdFiles.push("library/knowledge/public/overview/README.md");

writeIfMissing(
  join(args.root, "library", "knowledge", "public", "guides", "README.md"),
  "# Public Guides\n\nCustomer-facing how-to guides live here.\n",
) && createdFiles.push("library/knowledge/public/guides/README.md");

writeIfMissing(
  join(args.root, "library", "knowledge", "public", "faqs", "README.md"),
  "# Public FAQs\n\nCustomer-facing frequently asked questions live here.\n",
) && createdFiles.push("library/knowledge/public/faqs/README.md");

writeIfMissing(
  join(args.root, "library", "knowledge", "private", "standards", "README.md"),
  "# Documentation Standards\n\nProject documentation standards live here.\n",
) && createdFiles.push("library/knowledge/private/standards/README.md");

writeIfMissing(
  join(args.root, "library", "knowledge", "private", "standards", "documentation-framework.md"),
  readTemplate(args.root, "documentation-framework.md", "# Documentation Framework\n\nDefine project documentation rules here.\n"),
) && createdFiles.push("library/knowledge/private/standards/documentation-framework.md");

for (const [target, template] of Object.entries(readmes)) {
  const fallback = `# ${target.split("/").slice(-2, -1)[0] || "Library"}\n\nGenerated by That Git Life.\n`;
  if (writeIfMissing(join(args.root, target), readTemplate(args.root, template, fallback))) {
    createdFiles.push(target);
  }
}

jsonOut({
  ok: true,
  root: args.root,
  createdDirs,
  createdFiles,
});
